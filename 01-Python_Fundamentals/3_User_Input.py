# for integer input
a = int(input("Enter your age: "))
print("a: ",a,", type(a): ",type(a))

# for float input
b = float(input("Enter your cgpa: "))
print("b: ",b,", type(b): ",type(b))

# for string input
c = input("Enter your name: ")  # if we use only input() function, everything will consider as string
print("c: ",c,", type(c): ",type(c))

# for boolean input
d = bool(input("You're a idot: "))
print("d: ",d,", type(d): ",type(d))