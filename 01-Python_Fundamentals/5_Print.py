# by default sep=" " ,,that's why it will print a space after every (,)
print("Arin","Shabbir","Shakil")

# we can use anything instead of space by using sep="something"
print("Arin","Shabbir","Shakil", sep="<->")


# by default at the end of every print statemenet it is \n.. that's why if we print new statement it'll print in new line
# but we can change it
print("Hello,Arin", end="...")
print("Welcome to ultimate python course")   # both line print in a single line with ... separator